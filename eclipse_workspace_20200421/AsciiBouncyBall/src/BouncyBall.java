
public class BouncyBall {

	public static void main(String[] args) throws InterruptedException {

		while (true) {
			System.out.println("  O  ");
			System.out.println("     ");
			System.out.println("_____");
			System.out.println();
			
			Thread.sleep(100);
			addSpace();
			
			System.out.println("  |  ");
			System.out.println("  O  ");
			System.out.println("_____");
			
			Thread.sleep(100);
			addSpace();
			
			System.out.println("     ");
			System.out.println("     ");
			System.out.println("__O__");
 
			Thread.sleep(100);
			addSpace();
			
			System.out.println("      ");
			System.out.println("      ");
			System.out.println("__<>__");

			Thread.sleep(100);
			addSpace();
			
			System.out.println("     ");
			System.out.println("     ");
			System.out.println("__O__");

			Thread.sleep(100);
			addSpace();
			
			System.out.println("     ");
			System.out.println("  O  ");
			System.out.println("__|__");

			Thread.sleep(100);
			addSpace();
		}
			
	}

	public static void addSpace() {
		System.out.println( );
		System.out.println( );
		System.out.println( );
		System.out.println( );
		System.out.println( );
		System.out.println( );
		System.out.println( );
		System.out.println( );
		System.out.println( );
		System.out.println( );
		System.out.println( );
		System.out.println( );
		System.out.println( );
		System.out.println( );
		System.out.println( );
		System.out.println( );
		System.out.println( );
		System.out.println( );
		System.out.println( );
		System.out.println( );
		System.out.println( );
		System.out.println( );
		System.out.println( );
		System.out.println( );
		System.out.println( );
		System.out.println( );
	
	}
	
	
	
	
	
	
}
