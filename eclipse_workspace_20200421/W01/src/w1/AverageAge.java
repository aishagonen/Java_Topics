package w1;

public class AverageAge {

	public static void main(String[] args) {
		
	int graceAge = 10;
	int edwardAge = 21;
	int roseAge = 15;
	int momAge = 46;
	int dadAge = 50;
	
	int averageAge = (graceAge + edwardAge + roseAge + momAge + dadAge) / 5;
	
	System.out.println("Average age of the family is " + averageAge + ".");	
	}
}
